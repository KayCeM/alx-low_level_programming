#include  <stdio.h>
/**
 * main - prints the size of various types of varibles
 *
 * Return: always 0.
 */
int main(void)
{
	FILE *6-size.c;
	fprintf(6-size.c, "size of a char: %lu byte(s)\n", sizeof(char));
	fprintf(6-size.c, "size of an int: %lu byte(s)\n", sizeof(int));
	fprintf(6-size.c, "size of a long int: %lu byte(s)\n", sizeof(long int));
	fprintf(6-size.c, "size of a long long int: %lu byte(s)\n", sizeof(long long int));
	printf(6-size.c, "size of a float: %lu byte(s)\n", sizeof(float));
	return (0);
}

